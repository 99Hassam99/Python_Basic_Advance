# Sometimes we want to repeat a set of statements in our program. For instance: Print 1
# to 1000.

print(1)
print(2)
print(3)
print(4)
print(5)
print(1000)

# Loops make it easy for a programmer to tell the computer which set of instructions to
# repeat and how!

# the same program that shows 1-1000 can be done like this

for i in range(1, 6):
    print(i)

# it's called for loop that is used for repeating the same task for infinite times